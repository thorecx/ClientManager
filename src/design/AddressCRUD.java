    private void txtBuscarKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtBuscarKeyPressed
        // TODO add your handling code here:
        if(txtBuscar.getText() != ""){
        
        String pattern = txtBuscar.getText();
        String sql = "select * from addresses where address like '" + pattern + "%'";
        LimpiarTabla();
        try{
            cn = con.getConnection();
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            Object[] direcciones =  new Object[4];
            model = (DefaultTableModel) DTGdirecciones.getModel();
            while(rs.next()){
                direcciones[0] = rs.getInt("idaddress");
                direcciones[1] = rs.getString("address");
                direcciones[2] = rs.getString("idclient");
                direcciones[3] = rs.getString("idstatus");
                String[] cabecera = new String[]{"ID Direccion", "Direccion", "ID Cliente", "ID Estado"};
                model.setColumnIdentifiers(cabecera);
                model.addRow(direcciones);
            }
            DTGdirecciones.setModel(model);
        } catch(Exception e){
            
        }
        } else {
            Listar();
        }
    }//GEN-LAST:event_txtBuscarKeyPressed

