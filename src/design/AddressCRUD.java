    void Guardar(){
        
        String direccion = txtaDireccion.getText();
        String idclient = cbCliente.getSelectedItem().toString();
        String idstatus = cbEstado.getSelectedItem().toString();
        if(cbEstado.getSelectedIndex() == 0 || cbCliente.getSelectedIndex() == 0){
            JOptionPane.showMessageDialog(null, "Seleccione un estado y/o un cliente valido");
        } else{
            try {
              
              String sql = "insert into addresses (address, idclient, idstatus) values (?,?,?)";
              cn = con.getConnection();
              ps = cn.prepareStatement(sql);
              ps.setString(1, direccion);
              ps.setString(2, idclient);
              ps.setString(3, idstatus);
              res = ps.executeUpdate();
              if(res == 1){
                JOptionPane.showMessageDialog(null, "Direccion agregada satisfactoriamente");
                LimpiarTabla();

            } else{
                JOptionPane.showMessageDialog(null, "ERROR: el registro no fue guardado");;
            }
                
            } catch(Exception e){
            
            }
        }
    }
    
